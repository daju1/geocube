"fit.variogram" <-
function(object, model, id = c(0,0), fit.sills=T, fit.ranges=T)
{
	if (missing(object))
		stop("nothing to fit to")
	if (missing(model))
		stop("no model to fit")
	if (class(model) != "variogram.model")
		stop("model should be of class variogram.model (use vgm)")
	if (length(fit.sills) < length(model$model))
		fit.sills = rep(fit.sills, length(model$model))
	if (length(fit.ranges) < length(model$model))
		fit.ranges = rep(fit.ranges, length(model$model)) 
	# exclude Nugget models, as they don't have a range:
	fit.ranges = fit.ranges & (model$model != "Nug") 
	print(fit.sills)
	print(fit.ranges)
	.Call("gstat_load_ev", 
		as.integer(id), 
		object$np, 
		object$dist,
		object$gamma)
	load.variogram.model(model, id)
	ret = .Call("gstat_fit_variogram", 
		as.integer(id), 
		as.integer(7), 
		as.integer(fit.sills), 
		as.integer(fit.ranges))
	model$psill = ret[[1]]
	model$range = ret[[2]]
	print(paste("SSErr: ", ret[[3]]))
	model
}
