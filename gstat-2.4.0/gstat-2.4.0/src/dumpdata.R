		return(cbind(head,tail)[sel,])
	} 
	v
}
"variogram.formula" <-
function(formula, locations, data, ...)
{
	call = match.call()
	m = match.call(expand = F)
	m$method = m$model = m$x = m$y = m$contrasts = m$... = NULL
	m$drop.unused.levels = T
	m[[1]] = as.name("model.frame")
	m = eval(m, sys.parent())
	Terms = attr(m, "terms")
	xvars = as.character(attr(Terms, "variables"))
	if((yvar = attr(Terms, "response")) > 0)
		xvars = xvars[ - yvar]
	if(length(xvars) > 0) {
		xlevels = lapply(m[xvars], levels)
		xlevels = xlevels[!unlist(lapply(xlevels, is.null))]
		if(length(xlevels) == 0)
			xlevels = NULL
	}
	else xlevels = NULL
	weights = model.extract(m, weights)
	Y = model.extract(m, response)
	contrasts = NULL
	X = model.matrix(Terms, m, contrasts)
	variogram(Y, locations, X, ...)
}
