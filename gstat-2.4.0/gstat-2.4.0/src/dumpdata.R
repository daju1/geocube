"plot.variogram" <-
function(object, ylim, xlim, xlab = "distance", ylab = "semivariance", model,
		multipanel = T)
{
	if(missing(ylim) && min(object$gamma) >= 0)
		ylim = c(0, 1.04 * max(object$gamma))
	if(missing(xlim))
		xlim = c(0, 1.04 * max(object$dist))
