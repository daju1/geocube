"variogram" <-
function(y, locations, X, cutoff, width=cutoff/15, alpha = 0, beta = 0, 
	tol.hor = 180/length(alpha), tol.ver = 180/length(beta), cressie = F,
	dX = numeric(0))
{
	id1 = id2 = 0
	ret = NULL
	if (missing(cutoff))
		cw = numeric(0)
	else
		cw = c(cutoff, width)
	.Call("gstat_init", NULL)
	if (is.list(y) && is.list(locations)) {
		nvars = length(y)
		for (i in 1:nvars) {
			if(missing(X))
				Xloc = rep(1, length(y[[i]]))
			else
				Xloc = X[[i]]
			.Call("gstat_new_data", y[[i]], as.vector(locations[[i]]), 
				as.vector(Xloc))
		}
	} else {
		nvars = 1
		if(missing(X))
			X = rep(1, length(y))
		.Call("gstat_new_data", y, as.vector(locations), as.vector(X))
	}
	pos = 0;
	for (id1 in 1:nvars) {
		for (id2 in id1:nvars) {
			id = as.numeric(paste(id1, ".", id2, sep = ""))
			for (a in alpha) {
				for (b in beta) {
					direction = as.numeric(c(a, b, tol.hor, tol.ver))
					ret.call = .Call("gstat_variogram", 
						as.integer(c(id1 - 1, id2 - 1)), 
						as.numeric(cw), 
						as.numeric(direction),
						as.integer(cressie),
						as.numeric(dX))
					np = ret.call[[1]]
					sel = np > 0
					n.dir = length(sel[sel])
					if (n.dir > 0) {
						dist = ret.call[[2]]
						gamma = ret.call[[3]]
						dir.a = rep(a, n.dir)
						dir.b = rep(b, n.dir)
						ids = rep(id, n.dir)
						df = data.frame(np = np[sel], 
							dist = dist[sel], gamma = gamma[sel], 
							dir.hor = dir.a, dir.ver = dir.b, id = ids)
						if (pos > 0)
							ret[(pos+1):(pos+n.dir),] = df
						else
							ret = df
						pos = pos + n.dir
					}
				}
			}
		}
	}
	.Call("gstat_exit", NULL)
	ret
}
